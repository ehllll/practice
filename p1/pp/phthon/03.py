# 파이썬 변수

# 기본 선언
n = 700
print()

# 출력
print(n)
print(type(n)) #n의 자료형을 보여줌
print()

# 동시 선언
x = y = z = 700
print(x, y, z)
print()

#선언
var = 75
print()

# 재선언
var = "change value"
print()

#출력
print(var)
print(type(var))
print()

